<h2>Antes de tudo: o que você precisa saber</h2>
<p>Construir não é apenas escolher um terreno e contratar uma construtora. É um processo que começa muito antes da fundação — começa na escuta de você mesmo: como você vive, quais rituais fazem parte do seu cotidiano, quem vai habitar aquela casa com você, e qual a vida que você quer tornar possível dentro daquele espaço.</p>
<p>Antes de qualquer contrato ou planta, vale refletir:</p>
<ul>
  <li>Qual é o seu orçamento real? Inclua margem de imprevistos (15% a 20%).</li>
  <li>Você já tem o terreno? Se sim, conhece suas limitações legais e topográficas?</li>
  <li>Qual é o prazo? Construções bem-feitas raramente são entregues em menos de 18 meses.</li>
  <li>Como você quer viver? Ambientes integrados? Quintal? Home office?</li>
  <li>Você vai acompanhar a obra pessoalmente? Isso impacta a escolha do arquiteto.</li>
</ul>

