<h2>O terreno: a fundação de tudo</h2>
<p>O terreno não é apenas o chão onde sua casa vai ser construída — ele é um agente ativo do projeto. A topografia, a orientação solar, o entorno, as características do solo e as regras do município vão moldar, limitar e inspirar cada decisão de projeto.</p>

<h3>O que verificar antes de comprar</h3>
<ol>
  <li><strong>Legislação urbanística municipal.</strong> Recuo, taxa de ocupação, índice de aproveitamento, gabarito e zoneamento. Consulte a Prefeitura ou o plano diretor.</li>
  <li><strong>Registro e matrícula no cartório.</strong> Verifique se não há ônus, hipotecas ou litígios na matrícula. Consulte um advogado especialista em direito imobiliário.</li>
  <li><strong>Sondagem de solo (SPT).</strong> Revela a capacidade de carga do solo e define o tipo de fundação necessária — impacta diretamente o custo da obra.</li>
  <li><strong>Orientação solar e topografia.</strong> Boa orientação solar reduz gastos com climatização e aumenta o conforto ao longo de décadas.</li>
  <li><strong>Infraestrutura disponível.</strong> Água, esgoto, luz elétrica, acesso asfaltado. Em áreas rurais, a ausência eleva significativamente os custos.</li>
</ol>

<blockquote>"O terreno não é apenas o chão onde a casa vai repousar. É o primeiro traço do projeto — e o mais definitivo de todos."</blockquote>

<h2>Orçamento: construir com clareza</h2>

<figure>
  <img src="/images/blog/construir-casa-3-projeto.jpg" alt="Pessoa trabalhando em plantas arquitetônicas sobre uma mesa de madeira" loading="lazy" decoding="async" width="1280" height="720" />
  <figcaption>O projeto executivo completo é o documento que guia a execução e evita retrabalhos custosos.</figcaption>
</figure>

<p>O maior erro de quem inicia uma construção é subestimar os custos. O <strong>CUB (Custo Unitário Básico)</strong>, publicado mensalmente pelo Sinduscon de cada estado, é a referência mais utilizada — mas é apenas um ponto de partida. Em projetos de padrão médio-alto com arquitetura autoral, o custo por m² tende a superar consideravelmente o CUB padrão.</p>
<p>Além do custo de obra, considere:</p>
<ul>
  <li><strong>Projeto de arquitetura e complementares</strong> — normalmente 8% a 15% do custo total.</li>
  <li><strong>Taxas e licenças</strong> — aprovação na Prefeitura, RRT/ART, alvarás e habite-se.</li>
  <li><strong>Compra e transporte de materiais</strong> — especialmente para locais de difícil acesso.</li>
  <li><strong>Mobiliário e decoração</strong> — frequentemente ignorados no planejamento inicial.</li>
  <li><strong>Imprevistos</strong> — reserve sempre ao menos 15% do orçamento.</li>
</ul>

<h2>O projeto arquitetônico: da escuta ao traço</h2>
<p>Um projeto bem desenvolvido é o maior investimento de proteção que você pode fazer em uma construção. Ele orienta cada profissional envolvido na obra e previne conflitos, atrasos e gastos desnecessários. O processo se divide em fases:</p>
<ol>
  <li><strong>Levantamento e programa de necessidades.</strong> Reuniões de escuta para entender o modo de vida, as necessidades funcionais e os desejos antes de qualquer traço.</li>
  <li><strong>Estudo preliminar.</strong> Primeiras propostas de implantação, distribuição de ambientes e partido arquitetônico. Fase das grandes decisões conceituais.</li>
  <li><strong>Anteprojeto.</strong> Definição dos volumes, aberturas e materiais com mais precisão. Apresentado ao cliente para validação.</li>
  <li><strong>Projeto executivo.</strong> Detalhamento técnico completo: plantas, cortes, elevações, detalhes construtivos, especificação de materiais.</li>
  <li><strong>Projetos complementares.</strong> Estrutural, instalações elétricas, hidrossanitárias, SPDA, ar-condicionado — cada disciplina coordenada pelo arquiteto.</li>
</ol>

<h2>Como escolher o arquiteto certo</h2>

<figure>
  <img src="/images/blog/construir-casa-4-arquiteta.jpg" alt="Mulher sentada à mesa analisando plantas arquitetônicas" loading="lazy" decoding="async" width="1280" height="720" />
  <figcaption>A escolha começa antes da reunião — começa no estudo do trabalho e da forma de pensar do arquiteto.</figcaption>
</figure>

<p>Escolher o arquiteto é talvez a decisão mais importante de todo o processo. Você vai dividir meses de conversas, decisões, ajustes e expectativas — e o resultado final carrega inevitavelmente a identidade de quem projetou.</p>

<h3>6 critérios para uma escolha bem-feita</h3>
<ol>
  <li><strong>Repertório e portfólio.</strong> Estude os projetos realizados. A linguagem te inspira? O portfólio revela valores que palavras não traduzem.</li>
  <li><strong>Capacidade de escuta.</strong> Um bom arquiteto faz mais perguntas do que afirmações na primeira conversa. Ele quer entender como você vive.</li>
  <li><strong>Clareza contratual.</strong> Escopo, honorários, prazos e entregas descritos com clareza. Desconfie de propostas vagas ou combinações informais.</li>
  <li><strong>Registro no CAU.</strong> Verifique se o profissional tem registro ativo no Conselho de Arquitetura e Urbanismo (CAU BR).</li>
  <li><strong>Presença em obra.</strong> Visitas regulares ao canteiro são essenciais para garantir que o projeto seja executado conforme projetado.</li>
  <li><strong>Compatibilidade humana.</strong> Confiança e comunicação aberta são tão importantes quanto técnica. Você precisa se sentir confortável para questionar e sonhar junto.</li>
</ol>

<h3>Perguntas para a primeira conversa</h3>
<ul>
  <li>Quais projetos do seu portfólio se parecem com o que estou buscando?</li>
  <li>Como você organiza as fases de projeto e quais são as entregas de cada fase?</li>
  <li>Você acompanha a execução da obra? Com qual frequência visita o canteiro?</li>
  <li>Quem são os profissionais parceiros para os projetos complementares?</li>
  <li>Como você lida com imprevistos de obra ou mudanças de escopo?</li>
  <li>Posso falar com clientes de projetos anteriores?</li>
</ul>

<blockquote>"O arquiteto certo não é o mais premiado nem o mais caro. É aquele que entende seu modo de viver — e tem o repertório para traduzi-lo em espaço."</blockquote>

<h2>Da planta à obra: o que esperar da execução</h2>

<figure>
  <img src="/images/blog/construir-casa-5-obra.jpg" alt="Canteiro de obras de uma casa de madeira em construção" loading="lazy" decoding="async" width="1280" height="720" />
  <figcaption>A obra é a fase de maior complexidade — e onde um projeto bem desenvolvido faz toda a diferença.</figcaption>
</figure>

<p>Com o projeto aprovado na prefeitura e o alvará em mãos, começa a execução. A qualidade do projeto executivo se traduz diretamente em eficiência, menos retrabalho e custos mais previsíveis.</p>
<ul>
  <li><strong>Escolha a construtora com cuidado.</strong> Peça referências, visite obras em andamento.</li>
  <li><strong>Mantenha comunicação constante com o arquiteto.</strong> Qualquer desvio deve ser discutido antes de ser executado.</li>
  <li><strong>Registre tudo por escrito.</strong> Mudanças durante a obra geram custos adicionais — formalize em termos aditivos.</li>
  <li><strong>Visite a obra regularmente.</strong> O cliente atento é um aliado para identificar não-conformidades rapidamente.</li>
  <li><strong>Não antecipe pagamentos além do acordado.</strong> Vincule pagamentos a etapas concluídas.</li>
</ul>

<h2>Construir é um ato de confiança</h2>
<p>Construir sua casa é, no fundo, um ato de confiança: em si mesmo, nas escolhas que você fez, nas pessoas com quem você decidiu trabalhar — e no projeto que vai guiar cada tijolo.</p>
<p>Quando o projeto nasce de uma escuta verdadeira, quando o arquiteto entende o que você quer viver ali dentro, e quando a execução é feita com rigor — o resultado é uma casa que envelhece com beleza, que forma memória, e que se torna melhor com o tempo.</p>

